class Boolean_arrays {
    
    public static void main(String[] args) {
        
        boolean[] k;
        

        k = new boolean[2];
        k[0] = true;
        k[1] = false;

        if ((k[0]) && (k[1])) {
            System.out.println(1);

        }else {
            System.out.println(0);
        }
        
    }
}